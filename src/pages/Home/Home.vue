<script>
import BScroll from "better-scroll";
import Swiper from 'swiper';
import {mapState} from 'vuex';
export default {
    data(){
        return {
            goTop:false
        }
    },
    computed:{
        ...mapState(['focusList','cateList','tagList','policyDescList'])
    },
    methods:{
        handlemove(event){
            if(scrollY>200){
                this.goTop=true;
            }else{
                this.goTop=false;
            }
        },
        handleGoTop(){
            this.goTop=false;
            requestAnimationFrame(function fn(){
                if(scrollY!=0){
                    document.documentElement.scrollTop-=30;
                    requestAnimationFrame(fn);
                }
            })
        }
    },
    mounted() {
    new BScroll(".inner", {
      click: true,
      scrollX: true
    });
    window.addEventListener('scroll',this.handlemove)
    
    this.$store.dispatch('getFocusList',()=>{
        this.$nextTick(()=>{
                new Swiper('.swiper-container-horizontal',{
                autoplay:{
                    disableOnInteraction: false
                },
                loop:true,
                pagination:{
                    el:'.swiper-pagination-clickable'
                },
            });
        })
    });
    this.$store.dispatch('getCateList');
    this.$store.dispatch('getTagList');
    this.$store.dispatch('getPolicyDescList');
  }
};
</script>

